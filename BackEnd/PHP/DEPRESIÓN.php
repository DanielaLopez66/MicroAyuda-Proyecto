        <h2 class="v72_46">Síntomas</h2>




        <div class="muñeca1"><img src="/imge/chicaF.png" alt=""></div>
        <span class="texto1">
            La depresión es una enfermedad común pero grave que afecta la
            vida diaria y la capacidad para trabajar, dormir, estudiar, comer y disfrutar de la vida. Sus causas son una
            combinación de factores genéticos, biológicos, ambientales y psicológicos.

            Algunos tipos de depresión tienden a presentarse en familias, aunque también puede ocurrir en personas sin
            antecedentes familiares de la enfermedad. Los síntomas de la depresión no son iguales en todas las personas;
            la gravedad, frecuencia y duración de estos varían según la persona y su caso particular.
        </span>
        <div class="v72_49"></div>
        <div class="v72_50"></div>
        <p class="v72_47">
            Los signos y síntomas de depresión en los adolescentes incluyen
            cambios en su actitud y comportamiento previos, lo que puede causar angustia y dificultades significativas
            en la escuela, el hogar, las actividades sociales y otros aspectos de la vida.
        </p>
        <p class="v72_48">Los síntomas de la depresión en los adultos incluyen sentimientos persistentes de
            tristeza, vacío o desesperanza, irritabilidad o frustración, y pérdida de interés en actividades que antes
            disfrutaban. También pueden experimentar fatiga, cambios en el apetito y peso, problemas para dormir,
            dificultad para concentrarse, y sentimientos de inutilidad o culpa excesiva. Pensamientos recurrentes de
            muerte o suicidio, dolores físicos inexplicables, ansiedad, agitación, y aislamiento social son comunes. Si
            estos síntomas persisten, es crucial buscar ayuda profesional.
        </p>
